    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String contraseña, usuario;
        usuario = txtUsuario.getText();
        contraseña = txtContraseña.getText();
        Usuario u = cad_usuario.buscar(usuario);
        Administrador a = cad_administrador.buscar(usuario);
        if(u == null && a == null){
            JOptionPane.showMessageDialog(null, "No existe el usuario");
        }else{
            if(u!=null){
                if(u.getContraseña().equals(contraseña)){
                    JOptionPane.showMessageDialog(null, "Bienvenido " + u.getNombre() + " " + u.getApellido());
                    PanelUsuario panelUsuario = new PanelUsuario();
                    panelUsuario.setVisible(true);
                    this.dispose();
                }else{
                    JOptionPane.showMessageDialog(null, "Contraseñ incorrecta");
                }
            }
            if(a!=null){
                if(a.getContraseña().equals(contraseña)){
                    JOptionPane.showMessageDialog(null, "Bienvenido " + a.getNombre() + " " + a.getApellido());
                    PanelUsuario panelUsuario = new PanelUsuario();
                    panelUsuario.setVisible(true);
                    this.dispose();
                }else{
                    JOptionPane.showMessageDialog(null, "Contraseñ incorrecta");
                }
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

